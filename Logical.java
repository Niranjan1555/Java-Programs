public class Logical {
    public static void main(String[] args) {
        int a = 50;
        int b = 20;

        if(a > b && a > 30){
            System.out.println("a is greater than b");
        }else if (a < b || a < 30){
            System.out.println("a is lesser than b");
        }
        else{
            System.out.println("Nothing is Possible");
        }
    }
 }
    

